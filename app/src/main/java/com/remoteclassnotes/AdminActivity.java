    @Override
    protected void onActivityResult(int requestCode, int resultCode, Intent data) {
        super.onActivityResult(requestCode, resultCode, data);

        if (requestCode == READ_REQUEST_CODE && resultCode == Activity.RESULT_OK) {
            Uri uri;
            if (data != null) {
                uri = data.getData();
                final String fileName = getFileName(uri);

                Uri file = Uri.fromFile(new File(getPath(this, uri)));
                final UploadTask uploadTask = storageReference.child(fileName).putFile(file);
                mUserField.setText(fileName);
                Toast.makeText(this, "Upload started", Toast.LENGTH_SHORT).show();

                uploadTask.addOnSuccessListener(new OnSuccessListener<UploadTask.TaskSnapshot>() {
                    @Override
                    public void onSuccess(UploadTask.TaskSnapshot taskSnapshot) {
                        String downloadUrl = taskSnapshot.getDownloadUrl().toString();
                        NoteFile noteFile = new NoteFile(fileName, downloadUrl);
                        databaseReference.child("files").push().setValue(noteFile);
                        Toast.makeText(AdminActivity.this, "Uploaded", Toast.LENGTH_SHORT).show();
                    }
                }).addOnFailureListener(new OnFailureListener() {
                    @Override
                    public void onFailure(@NonNull Exception e) {
                        Toast.makeText(AdminActivity.this, "Upload failed", Toast.LENGTH_SHORT).show();
                    }
                });

            }
        }
    }

